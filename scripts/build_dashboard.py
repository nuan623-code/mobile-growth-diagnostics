#!/usr/bin/env python3
"""Render a single-file, offline, light-theme HTML dashboard.

Design choice that matters: the dashboard is rendered as **static HTML** here in
Python — including inline SVG charts — so it displays correctly even when
JavaScript is blocked (sandboxed preview panels, strict CSP, `file://`, offline).
There is NO CDN dependency and NO required runtime JS. The bundled <script> in
the template is *progressive enhancement only* (collapse/expand, anomaly filters,
drill-down); with it disabled, everything is simply shown expanded.

Inputs (same as before):
  --data       unified.json     (transform.py: meta + rows)
  --anomalies  anomalies.json   (detect_anomalies.py)
  --preflight  preflight.json   (validate_sample.py)
  --insights   insights.json    (WRITTEN BY CLAUDE — schema documented below)

insights.json schema (all optional; provide as much as you can):
{
  "language": "zh"|"en", "title": "...",
  "period": {"from":"...","to":"...","compare_to":"..."},
  "executive_summary": {"health_score":0-100,"takeaways":[...],
                        "top_anomalies":[...],"top_actions":[...]},
  "kpis": [{"label","value","delta_pct","spark":[...],"status":"good|warn|bad","note",
            "benchmark":"optional 'vs benchmark' line, e.g. 'D1 median 12% · Casino/VN/android'"}],
  "anomaly_notes": {"<index>": "plain-language read"},
  "trends": [{"label","dates":[...],"series":{"current":[...],"compare":[...]},
              "as_percent":bool,"note"}],
  "breakdown": {"primary_dim","child_dim","columns":[...],
                "rows":[{"key","values":{},"children":[{"key","values":{}}]}],"note"},
      # a cell value may also be {"v": "-87%", "status": "good|warn|bad"} —
      # rendered colored; use for "vs benchmark" delta columns.
  "breakdowns": [{"title","primary_dim","child_dim","columns","rows","note","group"}],
      # multiple breakdown tables, each its own section — use for the deep
      # report's 分国家 / 分渠道 / 分平台 / ROI / 停留时长 / 分维度留存 tables.
      # "title" names the section; "group" defaults to "analysis".
  "cohorts": {"periods":[...],"retention":[{"label","values":[...],"dashed":bool}],
              "ltv":[{"label","values":[...]}],"note"},
      # "dashed": true renders a gray dashed reference curve — use it for
      # industry benchmark medians (references/benchmarks_2025h2.json).
  "events": {"summary":[{...same shape as kpis...}],
             "funnel":{"stages":[{"label","value","rate_from_prev":0-1}],"note"},
             "top_events":{"columns":[...],"rows":[{"key","values":{}}],"note"},
             "trends":[{...same shape as trends...}],"note"},
      # Events panel. Omit the key entirely when event data wasn't pulled —
      # the builder then renders an honest empty-state card instead.

The dashboard renders as three panels (tabs with JS, stacked sections without):
overview (exec summary, KPIs, recommendations) · analysis (anomalies, trends,
breakdown, cohorts, validation, appendix) · events.
  "recommendations": [{"text","basis","impact","confidence","priority"}],
  "appendix": {"params":{},"degraded_metrics":[...],"notes":"..."}
}

Usage:
  python build_dashboard.py --data unified.json --anomalies anomalies.json \
      --preflight preflight.json --insights insights.json --out dashboard.html
"""
